def make_user_message(task: str, text: str) -> str:
    '''Сформировать юзер сообщение для задачи'''

    task_instructions = {
        "ner": f"Извлеки именованные сущности из следующего текста:\n\n{text}",
        "pos": f"Определи части речи для каждого токена в следующем тексте:\n\n{text}",
        "lemma": f"Лемматизируй каждое слово в следующем тексте:\n\n{text}",
    }

    if task not in task_instructions:
        raise ValueError(f"Неизвестная задача: {task}. Допустимые: ner, pos, lemma")
    
    return task_instructions[task]


SYSTEM_PROMPTS = {
    "llm_only": {
        "ner": LLM_ONLY_NER_SYSTEM,
        "pos": LLM_ONLY_POS_SYSTEM,
        "lemma": LLM_ONLY_LEMMA_SYSTEM,
    },
    "tool_assisted": {
        "ner": TOOL_ASSISTED_NER_SYSTEM,
        "pos": TOOL_ASSISTED_POS_SYSTEM,
        "lemma": TOOL_ASSISTED_LEMMA_SYSTEM,
    },
}

def get_system_prompt(mode: str, task: str) -> str:
    '''Получить системный промпт по режиму и задаче'''
    return SYSTEM_PROMPTS[mode][task]